function build_filters(programs) {
  var filters = {
    'steam': {
      'options':[],
      'name':'steam',
      'label':'Category',
      'value':'',
      'class':'category'
    },
    'age_group': {
      'options':[],
      'name':'age_group',
      'label':'Age',
      'value':'',
      'class':'age'
    }
  }

  // Build options based on program data.
  _.each(programs, function(p) {
    _.each(filters, function(f) {
      if ( ! _.contains(f['options'], p[f['name']])) {
        filters[f['name']]['options'].push(p[f['name']]);
      }
    });
  });

  return filters;
}

function apply_filter(programs, filters) {
  var properties = {}

  _.each(filters, function(f) {
    if (f['value'] != '' && _.contains(filters[f['name']]['options'], f['value'])) {
      properties[f['name']] = f['value'];
    }
  });

  return (_.isEmpty(properties)) ? programs : _.where(programs, properties);
}